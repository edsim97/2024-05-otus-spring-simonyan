import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

@DisplayName("Контроллер с методами для работы с авторами должен")
@WebMvcTest(AuthorController.class)
public class AuthorControllerTest {

    @Autowired
    private MockMvc mvc;

    @Autowired
    private ObjectMapper mapper;

    @MockBean
    private AuthorService authorService;

    @DisplayName("возвращать всех авторов")
    @Test
    void shouldReturnAllAuthors() throws Exception {

        List<Author> authorList = List.of(
            new Author(1, "Test1"),
            new Author(2, "Test2"),
            new Author(3, "Test3")
        );
        given(authorService.findAll()).willReturn(authorList);

        mvc.perform(get("/api/author"))
            .andExpect(status().isOk())
            .andExpect(MockMvcResultMatchers.content().json(mapper.writeValueAsString(authorList)));
    }
}
